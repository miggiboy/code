    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(StoreAdvertisementRequest $request)
    {
        $advertisement = Advertisement::create(
            $request->except('images', 'screenshot')
        );

        foreach ($request->file('images') as $image) {
            $advertisement->addMedia($image)->toMediaCollection('images');
        }

        if ($image = $request->file('screenshot')) {
            $advertisement->addMedia($image)->toMediaCollection('screenshot');
        }

        return redirect()->route('advertisements.index')->withMessage('Реклама добавлена');
    }

